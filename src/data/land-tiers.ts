import { imageUrls } from "./image-urls";
import { oracleIds, type CardName } from "./oracle-ids";

export type Card = {
  type: "card";
  name: string;
  oracleId: string;
  imageUrl: string;
};

export type CardCycle = {
  type: "card-cycle";
  name: string;
  representative: string;
  cards: Card[];
};

export type Tier = {
  name: string;
  cards: (Card | CardCycle)[];
};

export type LandTiers = Tier[];

const card = (name: CardName): Card => ({
  type: "card",
  name,
  oracleId: oracleIds[name],
  imageUrl: imageUrls[name],
});

const cycle = (name: string, representative: CardName, cardNames: CardName[]): CardCycle => ({
  type: "card-cycle",
  name,
  representative,
  cards: cardNames.map(card),
});

